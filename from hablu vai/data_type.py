# string
name = "Hafizur Rahman"
print(type(name))

# int
age = 23
print(type(age))

# float
height = 5.9
print(type(height))

# bool
isMuslim = True
print(type(isMuslim))

# complex (must have to use letter 'j')
isComplex = 7j
print(type(isComplex))

# none type data
x = None
print(type(x))

# list type
listType = [1, 2, 3, 4]
print(type(listType))

# tuple
# immutable
tupleType = ('hafiz', 'rafi', 'nishat')
print(type(tupleType))

# range
rangeType = range(10)
print(type(rangeType))
